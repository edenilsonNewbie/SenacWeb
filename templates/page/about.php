    <h1>Sobre</h1>

    <p>Em 1998, Edenilson, nasceu e a partir daí criado por neo-zeolandeses, passou sua infância vivendo em vários países, onde o maior período aconteceu aos 7 anos, quando viveu sozinho no Himalaia, sobrevivendo a base de alimentos encontrados no local.</p>
    <p>Em sua vida adulta, fez um saite e ficou milionário.</p>

